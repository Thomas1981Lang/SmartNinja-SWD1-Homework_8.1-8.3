mood = input('How are you?: ')

if mood == 'happy':
    print("It is great to see you happy!")
elif mood == 'nervous':
    print("Take a deep breath 3 times.")
elif mood == 'sad':
    print("Studies have shown that the very act of smiling can improve your mood and make you feel happier")
elif mood == 'excited':
    print("Good to hear that you are excited")
elif mood == 'relaxed':
    print("Being relaxed is great")
else:
    print("I don't recognize this mood")